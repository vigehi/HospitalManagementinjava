    private void table_update() {
        int CC;
        try {
             

           Class.forName("com.mysql.jdbc.Driver");
           con1 = DriverManager.getConnection("jdbc:mysql://localhost/hosi","root","");
           pst = con1.prepareStatement("SELECT * FROM doctor");
             ResultSet Rs = pst.executeQuery();  
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("id"));
                    v2.add(Rs.getString("name"));
                    v2.add(Rs.getString("specialization"));
                    v2.add(Rs.getString("qualification"));
                   v2.add(Rs.getString("fee"));
                   v2.add(Rs.getString("email"));
                   v2.add(Rs.getString("room"));

                }
                DFT.addRow(v2);
            }
            } 
        catch (ClassNotFoundException ex) {
            Logger.getLogger(viewDoctor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(viewDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


    
    
